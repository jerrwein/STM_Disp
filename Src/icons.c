/******************************************************************************
* OLED Glyph Maps
*
* - max glyph size is 128(wide) x 64(high) pixels, 1-bit per pixel
* - vertical addressing mode is used so data is written one column at a time,
*	from top to bottom, left to right
* - coordinates (0,0) indicate the upper left corner of glyph
* - rows are divided into pages(bytes): 8 rows(bits) per page
* - lsb of page byte is upper row pixel, msb is lower row pixel
* - byte[0] = pixel width of glyph map (number of x-pixels or columns)
* - byte[1] = pixel height of glyph map (number of y-pixels or rows)
*
* byte	|  0  |  1   | 2  | 3  | 4  |
* bit	|width|height| D0 | D1 | D2 | ...
*
*			Columns/Segments
*	Rows	| 0 | 1 | 2 | ...
*	--------|---|---|---|---
*	 0		|lsb|   |   |
*	 1		|   |   |   |
*	 2		|   |   |   |
*	 3		|D0 |D8 |D16| ...
*	 4		|   |   |   |
*	 5		|   |   |   |
*	 6		|   |   |   |
*	 7		|msb|   |   |
*	--------|---|---|---|---
*	 8		|lsb|   |   |
*	 9		|   |   |   |
*	10		|   |   |   |
*	11		|D1 |D9 |D17| ...
*	12		|   |   |   |
*	....
*	....
*	59		|   |   |   |
*	60		|D7 |D15|D23| ...
*	61		|   |   |   |
*	62		|   |   |   |
*	63		|msb|   |   |
*	--------|---|---|---|---
*
*
* example, 5x9 glyph:
*----------------------------------------------------------
*
*	column		0  1  2  3  4
*	row  0		-  -  -  -  -  lsb
*	row  1		-  -  X  -  -
*	row  2		-  X  X  -  -
*	row  3		-  -  X  -  -
*	row  4		-  -  X  -  -
*	row  5		-  -  X  -  -
*	row  6		-  -  X  -  -
*	row  7		-  X  X  X  -  msb
*	row  8		-  -  -  -  -
*
*	data[0]:  5		(width, num columns/y-pixels)
*	data[1]:  9		(height, num rows/x-pixels)
*	data[2]:  0x00	(col 0, row 0-7)
*	data[3]:  0x00	(col 0, row 8)
*	data[4]:  0x84	(col 1, row 0-7)
*	data[5]:  0x00	(col 1, row 8)
*	data[6]:  0xfe	(col 2, row 0-7)
*	data[7]:  0x00	(col 2, row 8)
*	data[8]:  0x80	(col 3, row 0-7)
*	data[9]:  0x00	(col 3, row 8)
*	data[10]: 0x00	(col 4, row 0-7)
*	data[11]: 0x00	(col 4, row 8)
*
******************************************************************************/

