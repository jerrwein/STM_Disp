/******************************************************************************
* Font5x7_GetGlyph
*	Return pointer to a character's glyph map from table.
*
* ch: ascii value of character
******************************************************************************/
const uint8_t* Font5x7_GetGlyph (uint8_t ch)
{
	return (mk_font5x7[ch]);
}

/******************************************************************************
* Font5x7_CharWidth
*	Return a character's pixel width.
*
* ch: ascii value of character
******************************************************************************/
uint32_t Font5x7_CharWidth (uint8_t ch)
{
	return ((uint32_t)mk_font5x7[ch][FONT5X7_WIDTH_BYTE]);
}

/******************************************************************************
* Font5x7_StringWidth
*	Return a character string's pixel width.
*
* str: ascii string
******************************************************************************/
uint32_t Font5x7_StringWidth (const uint8_t *str)
{
	uint32_t size = 0;
	
	while (*str)
	{
		size += mk_font5x7[*str][FONT5X7_WIDTH_BYTE];
		//add 1 for the space between characters
		if (*(++str))
			size++;
	}
	return (size);
}
